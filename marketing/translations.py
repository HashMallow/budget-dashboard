TRANSLATIONS: dict[str, dict[str, str]] = {"fa": FA}


def adapt_bidi_ui(text: str, lang: str) -> str:
    """Flip navigation arrows for RTL reading order in Persian UI copy."""
    if lang != "fa" or not text:
        return text
    return text.replace(" → ", " ← ")


def translate(text, lang: str) -> str:
    """Return the localized string for ``text`` in ``lang`` (English fallback)."""
    source = "" if text is None else str(text)
    if lang == "en":
        return source
    translated = TRANSLATIONS.get(lang, {}).get(source, source)
    return adapt_bidi_ui(translated, lang)
